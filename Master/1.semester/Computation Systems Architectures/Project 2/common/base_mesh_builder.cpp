BaseMeshBuilder::BaseMeshBuilder(unsigned gridEdgeSize, const std::string &name)
    : mGridSize(gridEdgeSize)
    , mGridResolution(0.0f)
    , mIsoLevel(0.0f)
    , mBuilderName(name)
    , mIsInBatchMode(false)
{
}

BaseMeshBuilder::~BaseMeshBuilder()
{

}

void BaseMeshBuilder::buildMesh(const ParametricScalarField &inputField, const std::string &meshFilename)
{
    Vec3_t<float> fieldSize = inputField.getSize();
    mGridResolution = std::max(fieldSize.x, std::max(fieldSize.y, fieldSize.z)) / float(mGridSize);
    mIsoLevel = inputField.getIsoLevel();

    if(mIsInBatchMode)
    {
        std::cout << mBuilderName << ";"
                  << inputField.GetFilename() << ";"
                  << meshFilename << ";"
                  << mGridSize << ";"
                  << inputField.getIsoLevel() << ";"
                  << inputField.getPoints().size() << ";"
                  << omp_get_max_threads() << ";" << std::flush;
    }
    else
    {
        std::cout << "======================== Marching Cubes Mesh Builder ==========================" << std::endl;
        std::cout << "Mesh Builder:        " << mBuilderName << std::endl;
        std::cout << "Input Field File:    " << inputField.GetFilename() << std::endl;
        std::cout << "Output Mesh File:    " << meshFilename << std::endl;
        std::cout << "Grid Size:           " << mGridSize << std::endl;
        std::cout << "Iso Level:           " << inputField.getIsoLevel() << std::endl;
        std::cout << "Field Elements:      " << inputField.getPoints().size() << std::endl;

        std::cout << "============================= Building the Mesh ===============================" << std::endl;
        std::cout << "Number of Threads:   " << omp_get_max_threads() << std::endl;
    }

    auto startTime = PerfClock_t::now();

    unsigned totalTriangles = marchCubes(inputField);

    auto elapsedTime = PerfClockDurationMs(PerfClock_t::now() - startTime).count();

    if(mIsInBatchMode)
    {
        std::cout << elapsedTime << ";"
                  << totalTriangles << ";" << std::flush;
    }
    else
    {
        //std::cout << "=================================== DONE ======================================" << std::endl;
        std::cout << "Elapsed Time:        " << elapsedTime << " ms" << std::endl;
        std::cout << "Mesh Triangle Count: " << totalTriangles;
    }

    if(!meshFilename.empty())
    {
        unsigned bytesWritten = storeMeshFile(meshFilename, totalTriangles);

        if(mIsInBatchMode)
        {
            std::cout << bytesWritten;
        }
        else
        {
            std::cout << std::endl;
            std::cout << "=========================== Writting Output File ==============================" << std::endl;
            std::cout << "Output File Size:    " << bytesWritten / 1024 << " KB" << std::endl;
            std::cout << "=================================== DONE ======================================";
        }
    }

    std::cout << std::endl;
}

void BaseMeshBuilder::transformCubeVertices(const Vec3_t<float> &pos, const CubeCornerVerts_t &normVerts,
                                            CubeCornerVerts_t &outMeshSpaceVerts)
{
    // Translate and scale all 8 corners of the normalized cube.
    for(unsigned i = 0; i < 8; ++i)
    {
        outMeshSpaceVerts[i].x = (pos.x + normVerts[i].x) * mGridResolution;
        outMeshSpaceVerts[i].y = (pos.y + normVerts[i].y) * mGridResolution;
        outMeshSpaceVerts[i].z = (pos.z + normVerts[i].z) * mGridResolution;
    }
}

void BaseMeshBuilder::interpolateVertex(const Vec3_t<float> &v0, float l0,
                                        const Vec3_t<float> &v1, float l1, Vec3_t<float> &out)
{
    // Linear interpolation coefficient in (0, 1) range generated from required
    // isosurface value and field values at endpoints (l0, l1) of the edges.
    const float interpCoeff = (mIsoLevel - l0) / (l1 - l0);

    // Compute new point by linear interpolation between endpoints of the edge (v0, v1).
    out.x = v0.x * (1.0f - interpCoeff) + v1.x * interpCoeff;
    out.y = v0.y * (1.0f - interpCoeff) + v1.y * interpCoeff;
    out.z = v0.z * (1.0f - interpCoeff) + v1.z * interpCoeff;
}

unsigned BaseMeshBuilder::buildCube(const Vec3_t<float> &position, const ParametricScalarField &field)
{
    // 1. Compute cube corner positions in the "field space".
    CubeCornerVerts_t cubeCorners;
    transformCubeVertices(position, sc_vertexNormPos, cubeCorners);

    // 2. Evaluate field value at each corner of the cube and identify appropriate
    //    variant of "marching cube" by checking which of the corners are above/below
    //    the isosurface.
    unsigned cubeIndex = 0;
    std::array<float, 8> cubeCornerValues;
    for(unsigned i = 0; i < 8; ++i)
    {
        cubeCornerValues[i] = evaluateFieldAt(cubeCorners[i], field);
        cubeIndex |= unsigned(cubeCornerValues[i] < mIsoLevel) << i;
    }

    // 3. Check if all corners are below/above the isosurface as no polygons are
    //    generated in that case (surface does *NOT* intersect this cube).
    if(cubeIndex == 0 || cubeIndex == 255)
        return 0;

    // 4. (OPTIONALLY) Interpolate point at each edge of the cube so that it lies
    //    exactly at the point where isosurface intersects the edge (using linear interpolation).
    //    This gives smoother surface compared to using exactly 1/2 between endpoints of the edge.
    std::array<Vec3_t<float>, 12> edgeVertices;
    for(unsigned i = 0; i < 12; ++i)
    {
        const unsigned v0Idx = i % 8;
        const unsigned v1Idx = sc_vertexAdjIds[i];

        interpolateVertex(cubeCorners[v0Idx], cubeCornerValues[v0Idx],
                          cubeCorners[v1Idx], cubeCornerValues[v1Idx], edgeVertices[i]);
    }

    unsigned emitedTriangles = 0;

    // 5. Using "marching cubes" distionary and interpolated "mid-edge" points
    //    generate 1 to 5 triangles approximating the surface inside of the currently
    //    evaluated "marching cube".
    for(unsigned i = 0; i < 5; ++i)
    {
        if(sc_triIndexTable[cubeIndex][3*i] == -1)
            break;

        std::array<Vec3_t<float>, 3> triangle = {
            edgeVertices[unsigned(sc_triIndexTable[cubeIndex][3*i + 0])],
            edgeVertices[unsigned(sc_triIndexTable[cubeIndex][3*i + 1])],
            edgeVertices[unsigned(sc_triIndexTable[cubeIndex][3*i + 2])]
        };

        // Store the newly generated triangle.
        emitTriangle(triangle);
        emitedTriangles++;
    }

    return emitedTriangles;
}

unsigned BaseMeshBuilder::storeMeshFile(const std::string &meshFilename, unsigned count)
{
    // Create new file to store mesh in Wavefront *.obj format.
    std::ofstream meshFile(meshFilename.c_str(), std::ios::out | std::ios::trunc);

    // Get pointer to array of sequentially stored triangles.
    const std::array<Vec3_t<float>, 3> *triangles = getTrianglesArray();

    meshFile << std::setprecision(9);

    // Store all verties (corners of generated triangles) in the file as "v X Y Z".
    for(unsigned i = 0; i < count; ++i)
    {
        for(unsigned j = 0; j < 3; ++j)
            meshFile << "v " << triangles[i][j].x << " " << triangles[i][j].y << " " << triangles[i][j].z << std::endl;
    }

    // Store face indices connecting triples of verties toform actual triangles.
    // The triangles are stored sequentially, therefore only sequential numbers
    // are printed out here in the format: "f N N+1 N+2"
    for(unsigned i = 0; i < count; ++i)
    {
        unsigned triangleIdx = 3*i + 1;

        meshFile << "f " << triangleIdx + 0 << " " << triangleIdx + 1 << " " << triangleIdx + 2 << std::endl;
    }

    return unsigned(meshFile.tellp());
}

